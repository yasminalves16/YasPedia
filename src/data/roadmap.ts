export const roadmapCategories = roadmapPhases.map((phase) => phase.title);

export function getRoadmapPhaseById(id: string): RoadmapPhase | undefined {
  return roadmapPhases.find((phase) => phase.id === id);
}

export function getRoadmapPhaseByTitle(title: string): RoadmapPhase | undefined {
  return roadmapPhases.find((phase) => phase.title === title);
}

export function getEnabledTopicCount(phase: RoadmapPhase): number {
  return phase.topics.filter((topic) => topic.enabled).length;
}

export function getTotalRoadmapTopicCount(): number {
  return roadmapPhases.reduce((total, phase) => total + phase.topics.length, 0);
}

export function getEnabledRoadmapTopicCount(): number {
  return roadmapPhases.reduce(
    (total, phase) => total + getEnabledTopicCount(phase),
    0,
  );
}
